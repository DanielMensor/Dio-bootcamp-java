public class TiposVariaveis {
    public static void main(String[] args){
    
        byte b = 5; /*numero pequeno */
        short s = 100; /*numero pequeno */
        int i = 150; /* numero inteiro mais usado */
        long l = 150000000000l; /*numero inteiro grande */
        float f = 10.3f; /*ponto flutuante */ 
        double d = 5.2; /*ponto flutuante mais usado */
        char c = 'a'; /* um unico caracter */
        boolean boo = true; /* verdadeiro o u falso (true||false) */
        String str = "Olá Mundo"; /* String é uma clasee que representa conjunto de caracteres. */
        
    }
}
